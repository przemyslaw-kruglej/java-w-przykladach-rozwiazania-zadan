package zadanie55;

public class LiczeniePolFigur {
  public static double policzPoleKola(double promien) {
    return Math.PI * promien * promien;
  }

  public static double policzPoleKwadratu(double bok) {
    return bok * bok;
  }

  public static double policzPoleProstokata(double a, double b) {
    return a * b;
  }
}
